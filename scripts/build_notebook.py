# ----------------------------------------------------------------- execute + build
def split_lines(text: str):
    lines = text.splitlines(keepends=True)
    return lines if lines else [""]

ns: dict = {}
nb_cells = []
exec_count = 0
fig_i = 0
for ctype, src in CELLS:
    if ctype == "markdown":
        nb_cells.append({"cell_type": "markdown", "metadata": {}, "source": split_lines(src)})
        continue
    exec_count += 1
    outputs = []
    buf = io.StringIO()
    with contextlib.redirect_stdout(buf):
        try:
            exec(src, ns)
        except Exception as e:  # surface errors instead of failing silently
            print(f"!! ERROR: {type(e).__name__}: {e}")
    text = buf.getvalue()
    if text.strip():
        outputs.append({"output_type": "stream", "name": "stdout", "text": split_lines(text)})
    for num in plt.get_fignums():
        fig = plt.figure(num)
        png = io.BytesIO(); fig.savefig(png, format="png", bbox_inches="tight", dpi=110)
        b64 = base64.b64encode(png.getvalue()).decode()
        fig.savefig(FIGDIR / f"fig_{fig_i:02d}.png", bbox_inches="tight", dpi=110)
        fig_i += 1
        outputs.append({"output_type": "display_data", "data": {"image/png": b64}, "metadata": {}})
        plt.close(num)
    nb_cells.append({"cell_type": "code", "execution_count": exec_count,
                     "metadata": {}, "outputs": outputs, "source": split_lines(src)})

notebook = {
    "cells": nb_cells,
    "metadata": {
        "kernelspec": {"display_name": "Python 3", "language": "python", "name": "python3"},
        "language_info": {"name": "python", "version": "3.10"},
    },
    "nbformat": 4, "nbformat_minor": 5,
}
