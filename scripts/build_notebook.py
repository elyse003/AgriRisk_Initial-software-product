def label_risk(rain_a, cpi_c, fert_c):
    if rain_a < -0.8 and (cpi_c > 15 or fert_c > 30): return "High"
    if rain_a < -0.3 or cpi_c > 10 or fert_c > 20:    return "Medium"
    return "Low"

risk = pd.merge_asof(rain, cpi[["date","cpi_change"]], on="date")
risk = pd.merge_asof(risk, fert[["date","fert_change"]], on="date").dropna().reset_index(drop=True)
risk["risk_level"] = risk.apply(lambda r: label_risk(r.rainfall_anomaly, r.cpi_change, r.fert_change), axis=1)
print("Risk dataset:", risk.shape[0], "rows")
print(risk["risk_level"].value_counts())
risk.head()
